local M = {};

--- get nvim current mode string
---@return string
M.get_mode = function()
    local modes = {
        ["n"] = "NORMAL",
        ["no"] = "OP",
        ["nov"] = "OP",
        ["noV"] = "OP",
        ["no"] = "OP",
        ["niI"] = "NORMAL",
        ["niR"] = "NORMAL",
        ["niV"] = "NORMAL",
        ["v"] = "VISUAL",
        ["V"] = "V-LINE",
        [""] = "V-BLOCK",
        ["s"] = "SELECT",
        ["S"] = "SELECT",
        [""] = "V-BLOCK",
        ["i"] = "INSERT",
        ["ic"] = "INSERT",
        ["ix"] = "INSERT",
        ["R"] = "REPLACE",
        ["Rc"] = "REPLACE",
        ["Rv"] = "V-REPLACE",
        ["Rx"] = "REPLACE",
        ["c"] = "COMMAND",
        ["cv"] = "COMMAND",
        ["ce"] = "COMMAND",
        ["r"] = "ENTER",
        ["rm"] = "MORE",
        ["r?"] = "CONFIRM",
        ["!"] = "SHELL",
        ["t"] = "TERMINAL",
        ["nt"] = "NORMAL",
        ["null"] = "NONE",
    }
    return modes[vim.api.nvim_get_mode().mode]
end

return M;
